#include <iostream>
using namespace std;

int main() {
    // Declaring two integer variables
    int a = 6;
    int b = 2;
    // Declaring two floating-point variables
    float c = 6.4;
    float d = 2.2;

    // Performing operations on integers
    cout << "The arithemetic operations on integers are :"<<endl;
    cout << "a +  b = " << a + b << endl; //addition
    cout << "a - b = " << a - b << endl;  //subtraction
    cout << "a * b = " << a * b << endl;  //multiplication
    cout << "a / b = " << a / b << endl << endl;  //division
    // Performing operations on floating-point numbers
    cout << "The arithemetic operations on float variables are :"<<endl;
    cout << "c + d = " << c + d << endl;  //addition
    cout << "c - d = " << c - d << endl;  //subtraction
    cout << "c * d = " << c * d << endl;  //multiplication
    cout << "c / d = " << c / d << endl;  //division

    return 0;
}
