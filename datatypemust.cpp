#include <iostream>
#include <string>
using namespace std;

int main() {
    int age = 19;               // Integer data type
    float score = 95.5;        // Floating-point data type
    string name = "Manal";       // String data type
    bool is_she_genius = true;      // Boolean data type

    cout << "Name: " << name << endl;
    cout << "Age: " << age << endl;
    cout << "Score: " << score <<endl;
    cout << "Is She genius: " << (is_she_genius ? "Yes" : "No") << endl;

    return 0;
}
